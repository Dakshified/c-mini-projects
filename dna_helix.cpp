#include <iostream>
#include <unistd.h>
using namespace std;

int main() {
    string helix[6] = {
        "      A     T",
        "     A       T",
        "    G         C",
        "     G       C",
        "      C     G",
        "       C   G"
    };

    while (true) {
        for (int i = 0; i < 6; i++) {
            system("clear");
            cout << helix[i] << endl;
            usleep(200000); // delay
        }
    }
    return 0;
}
